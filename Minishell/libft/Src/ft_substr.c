#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	out_len;
	char	*save;
	char	*out;

	if (!s)
		return (NULL);
	if (ft_strlen(s) < start)
		return (ft_strdup(""));
	out_len = ft_strlen(s + start);
	if (out_len < len)
		len = out_len;
	out = malloc(len + 1);
	if (!out)
		return (NULL);
	save = out;
	s += start;
	while (*s && len--)
		*out++ = *s++;
	*out = '\0';
	return (save);
}
